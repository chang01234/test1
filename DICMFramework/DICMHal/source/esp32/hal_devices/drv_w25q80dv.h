/*! \file drv_pca9675.c
	\brief Winbond W25Q80DV flash driver

    3V 8Mbit serial flash memory with dual and quad SPI
*/

#ifndef DRV_W25Q80DV_H_
#define DRV_W25Q80DV_H_

#define W25Q80DV_PAGE_PROGRAM		0x02
#define W25Q80DV_READ				0x03
#define W25Q80DV_WRITE_ENABLE		0x06
#define W25Q80DV_FAST_READ			0x0b
#define W25Q80DV_READ_MANUFACTURER	0x90
#define W25Q80DV_SECTOR_ERASE		0x20
#define W25Q80DV_BLOCK_ERASE_32K	0x52
#define W25Q80DV_BLOCK_ERASE_64K	0xd8
#define W25Q80DV_READ_STATUS_REG_1	0x05
#define W25Q80DV_READ_STATUS_REG_2	0x35

#define W25Q80DV_SREG_BUSY			0x0001
#define W25Q80DV_SREG_WEL			0x0002
#define W25Q80DV_SREG_BP0			0x0004
#define W25Q80DV_SREG_BP1			0x0008
#define W25Q80DV_SREG_BP2			0x0010
#define W25Q80DV_SREG_TB			0x0020
#define W25Q80DV_SREG_SEC			0x0040
#define W25Q80DV_SREG_SRP0			0x0080
#define W25Q80DV_SREG_SRP1			0x0100
#define W25Q80DV_SREG_QE			0x0200
#define W25Q80DV_SREG_LB1			0x0800
#define W25Q80DV_SREG_LB2			0x1000
#define W25Q80DV_SREG_LB3			0x2000
#define W25Q80DV_SREG_CMP			0x4000
#define W25Q80DV_SREG_SUS			0x8000

#define W25Q80DV_PAGE_SIZE			256
#define W25Q80DV_SECTOR_SIZE		4096

#endif /* DRV_W25Q80DV_H_ */